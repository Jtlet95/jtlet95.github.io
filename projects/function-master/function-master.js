function keysToString(object) {
return Object.keys(object).join(" ");
}

//////////////////////////////////////////////////////////////////////
// Function 3 - Values to String /////////////////////////////////////
//////////////////////////////////////////////////////////////////////

function valuesToString(object) {
    let array = Object.values(object);
    
    for(let i = 0; i <= array.length-1; i++){
        if(typeof array[i] !== "string"){
            array.splice(i,1)
        }
    }
    return array.join(" ");
}

